	AREA   reset, DATA, READONLY
	EXPORT __Vectors
__Vectors
	DCD 0X10001000 
	DCD Reset_Handler
	ALIGN
	AREA mycode, CODE, READONLY
	EXPORT Reset_Handler
	ENTRY
Reset_Handler
	MOV R0,#0X15
	MOV R2,#1
	AND R1,R0,#0X0F
	MUL R3,R1,R2
	LSR R0,#4
	AND R1,R0,#0X0F
	MOV R6,#0X0A
	MUL R2,R6
	MUL R4,R2,R1
	ADD R5,R3,R4
STOP
	B STOP
	AREA mydata,DATA,READWRITE
DST DCD 0,0
	END
AREA   reset, DATA, READONLY
	EXPORT __Vectors
__Vectors
	DCD 0X10001000 
	DCD Reset_Handler
	ALIGN
	AREA mycode, CODE, READONLY
	EXPORT Reset_Handler
	ENTRY
Reset_Handler
	LDR R0,=0X12345678
	MOV R3,#01
	MOV R4,#0
	MOV R1,#8
UP	AND R2,R0,#0X0F
	MLA R4,R2,R3,R4
	MOV R5,#0X0A
	MUL R3,R5
	LSR R0,#4
	SUBS R1,#1
	BNE UP
STOP
	B STOP
	AREA mydata,DATA,READWRITE
DST DCD 0,0
	END

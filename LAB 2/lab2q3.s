	AREA reset, DATA, READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode, CODE, READONLY
	EXPORT Reset_Handler
	ENTRY
Reset_Handler
	LDR R11,=0x20000000
    MSR xPSR,R11 ;sets carry to 1 (initialization)
	MOV R10, #4
	LDR R0, = DST
	LDR R1, = SRC1
	LDR R2, = SRC2
UP	LDR R3, [R1], #4	
	LDR R4, [R2], #4
	SBCS R5, R3, R4
	STR R5, [R0], #4
	SUB R10, #1
	TEQ R10,#0
	BNE UP
STOP B STOP
SRC1 DCD  0x12345678,0x11111111,0xABCDEF12,0x0AAAAAAA
SRC2 DCD  0x12345678,0x10001000,0xF123F123,0x10101010
	AREA mydata, DATA, READWRITE
DST DCD 0, 0
	END
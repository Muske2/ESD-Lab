	AREA RESET,DATA,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0X10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R5,=SRC
	LDR R0,=DST
	MOV R1,#0
	MOV R2,#1
	LDR R3,[R5]
	STR R1,[R0],#4
	STR R2,[R0],#4
UP	ADD R4,R1,R2
	STR R4,[R0],#4
	MOV R1,R2
	MOV R2,R4
	SUBS R3,#1
	BNE UP
STOP B STOP
SRC DCD 10
	AREA mydata,DATA,READWRITE
DST DCD 0
	END
